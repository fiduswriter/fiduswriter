function parse(input) {
    return (input + '').split('')
}

function to(chart) {
    return function(input) {
        return chart[input]
    }
}

export const sup = function(input) {
    return parse(input).map(to(superscripts)).join('')
}

export const supChars = Object.keys(superscripts).join('')

export const sub = function(input) {
    return parse(input).map(to(subscripts)).join('')
}

export const subChars = Object.keys(subscripts).join('')
